<p>
  The star ratings used to evaluate health plans are based on the <a href= 'https://www.ncqa.org/hedis/measures/'>Healthcare Effectiveness Data and Information Set (HEDIS) measures</a>. The HEDIS program is administered on behalf of The <a href='https://www.cms.gov/'>Centers for Medicare & Medicaid Services (CMS)</a> by the <a href='https://www.ncqa.org/'>Nation Committe for Quality Assurance (NCQA)</a>, an independent nonprofit organization founded over 30 years ago with the goal of improving healthcare in the U.S.
</p><p>
  The HEDIS system aims to create a standardized system for assessing the quality of healthcare services across different aspects of care. They capture information on multiple overlapping aspects of healthcare delivery, including managing specific chronic diseases, women's health, prenatal care, vaccination rates, and support for behavioral health. 
</p><p>
  The measures for 2022 include 67 distinct measures focused on assessing treatment outcomes, preventive efforts, and patient experience.  The visualization below shows all of the current measures in the three existing market sectors: Commercial/Private plans (bought by individuals or by an employer), plans in the Medicare Advantage program, and Medicaid beneficiaries. 
